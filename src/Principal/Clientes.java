
package Principal;

public class Clientes {
    String Correo;
    String Nombre;
    String Apellido;
    String Contraseña;
    String Dpi;
    String Rol;
    String FechaNacimiento;
    String Genero;
    String Nacionalidad;
    String Sobrenombre;
    String Telefono;

    @Override
    public String toString() {
        return "Clientes{" + "Correo=" + Correo + ", Nombre=" + Nombre + ", Apellido=" + Apellido + ", Contrase\u00f1a=" + Contraseña + ", Dpi=" + Dpi + ", Rol=" + Rol + ", FechaNacimiento=" + FechaNacimiento + ", Genero=" + Genero + ", Nacionalidad=" + Nacionalidad + ", Sobrenombre=" + Sobrenombre + ", Telefono=" + Telefono + '}';
    }
    

    public Clientes() {
    }
    

    public Clientes(String Correo, String Nombre, String Apellido, String Contraseña, String Dpi, String Rol, String FechaNacimiento, String Genero, String Nacionalidad, String Sobrenombre, String Telefono) {
        this.Correo = Correo;
        this.Nombre = Nombre;
        this.Apellido = Apellido;
        this.Contraseña = Contraseña;
        this.Dpi = Dpi;
        this.Rol = Rol;
        this.FechaNacimiento = FechaNacimiento;
        this.Genero = Genero;
        this.Nacionalidad = Nacionalidad;
        this.Sobrenombre = Sobrenombre;
        this.Telefono = Telefono;
    }
    
    

    public String getCorreo() {
        return Correo;
    }

    public void setCorreo(String Correo) {
        this.Correo = Correo;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public String getApellido() {
        return Apellido;
    }

    public void setApellido(String Apellido) {
        this.Apellido = Apellido;
    }

    public String getContraseña() {
        return Contraseña;
    }

    public void setContraseña(String Contraseña) {
        this.Contraseña = Contraseña;
    }

    

    public String getRol() {
        return Rol;
    }

    public void setRol(String Rol) {
        this.Rol = Rol;
    }

    public String getFechaNacimiento() {
        return FechaNacimiento;
    }

    public void setFechaNacimiento(String FechaNacimiento) {
        this.FechaNacimiento = FechaNacimiento;
    }

    public String getGenero() {
        return Genero;
    }

    public void setGenero(String Genero) {
        this.Genero = Genero;
    }

    public String getNacionalidad() {
        return Nacionalidad;
    }

    public void setNacionalidad(String Nacionalidad) {
        this.Nacionalidad = Nacionalidad;
    }

    public String getSobrenombre() {
        return Sobrenombre;
    }

    public void setSobrenombre(String Sobrenombre) {
        this.Sobrenombre = Sobrenombre;
    }

   
   
}
       

       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
       
           
           
               
               
               
               
            









           



   
            
